const plus1 = document.querySelector("#plus1")
const mark1 = document.querySelector("#mark1");

plus1.addEventListener ("click" , () => {
    document.querySelector("#plus1").style.display = "none";
    document.querySelector("#plus1").style.marginTop = "-2rem";
    document.querySelector("#mark1").style.display = "block";
    document.querySelector("#mark1").style.marginTop = "-1.5rem";
    document.querySelector("#Ans1").style.display = "block";
})

mark1.addEventListener("click", () => {
    document.querySelector("#mark1").style.display = "none";
    document.querySelector("#plus1").style.display = "block";
    document.querySelector("#Ans1").style.display = "none";

})







